// need to have weakerAccess to allow use of parcel
@Parcel
public class TagDto implements ItemDto {

    Long id;
    String name;
    String color;

    boolean forMovies;
    boolean forSeries;

    public TagDto() {
    }

    public TagDto(Long id, String name, String color) {
        this.id = id;
        this.name = name;
        this.color = color;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
